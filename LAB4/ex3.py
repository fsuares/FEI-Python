lados = int(input("Digite o número de lados desejados: "))

if lados == 3:
    print("Triângulo.")

elif lados == 4:
    print("Quadrilátero.")

elif lados == 5:
    print("Pentágono.")

elif lados == 6:
    print("Hexágono.")

elif lados == 7:
    print("Heptágono.")

elif lados == 8:
    print("Octágono.")

elif lados == 9:
    print("Eneágono.")

elif lados == 10:
    print("Decágono.")

else:
    print("O programa não suporta a quantidade informada")